/**
 * Resourceful controller for interacting with answers
 */
const { validate } = use('Validator')

const Answer = use('App/Models/Answer')

class AnswerController {
  /**
   * Show a list of all answers.
   * GET answers
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async index ({ request, response, view }) {
  }

  
  /**
   * Create/save a new answer.
   * POST answers
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async store ({ request, response }) {

    const rules = {
      user_id: 'required',
      question_id: 'required',
    }

    const validation = await validate(request.all(), rules)

    if (validation.fails()) {
      if(validation._errorMessages[0].validation == "required"){
        return response.status(500).send({message: `${validation._errorMessages[0].field} tidak boleh kosong`})
      }
    }

    const {question_id,user_id,answer,attachment} = request.only([
      'question_id',
      'user_id',
      'answer',
      'attachment'
    ])

    const answers = await  Answer.create({
      question_id,
      user_id,
      answer,
      attachment
    })

    return response.status(200).send({message: "answers submitted", data:answers.answer || answers.attachment})
  }

  /**
   * Display a single answer.
   * GET answers/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async show ({ params, request, response, view }) {
  }

  /**
   * Render a form to update an existing answer.
   * GET answers/:id/edit
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async edit ({ params, request, response, view }) {
  }

  /**
   * Update answer details.
   * PUT or PATCH answers/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async update ({ params, request, response }) {
  }

  /**
   * Delete a answer with id.
   * DELETE answers/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   */
  async destroy ({ params, request, response }) {
  }
}

module.exports = AnswerController
